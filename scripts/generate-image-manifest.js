const fs = require('node:fs');
const path = require('node:path');
const vm = require('node:vm');

const ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(ROOT, 'assets', 'image-manifest.js');
const DAY_ART_FILES = [
  'assets/day/base-du-site-day.jpg',
  'assets/day/base-principale-fond-site-day.jpg',
  'assets/day/category-apero-day.jpg',
  'assets/day/category-petit-dejeuner-day.jpg',
  'assets/day/category-plats-day.jpg',
  'assets/day/category-sauces-bases-day.jpg',
  'assets/day/recipe-seafood-day.jpg'
];
const CHECK_ONLY = process.argv.includes('--check');

function read(file) {
  return fs.readFileSync(path.join(ROOT, file), 'utf8');
}

function loadRecipes() {
  const context = { window: {} };
  vm.createContext(context);
  vm.runInContext(read('recipes.js'), context, { filename: path.join(ROOT, 'recipes.js') });
  return context.window.RECIPES || {};
}

function normalizeKey(file) {
  return file.replace(/\\/g, '/').replace(/^\/+/, '').replace(/\?.*$/, '');
}

function recipeCardPath(image) {
  return normalizeKey(image)
    .replace(/^assets\/recipe-images-optimized\//, 'assets/recipe-card-images/')
    .replace(/\.(?:png|jpe?g|webp)$/i, '.jpg');
}

function imageSourcePath(image) {
  return normalizeKey(image)
    .replace(/^assets\/recipe-images-optimized\//, 'assets/recipe-images/')
    .replace(/\.(?:jpe?g|webp)$/i, '.png');
}

function jpegSize(buffer) {
  if (buffer[0] !== 0xFF || buffer[1] !== 0xD8) return null;
  let offset = 2;
  while (offset < buffer.length - 9) {
    if (buffer[offset] !== 0xFF) {
      offset += 1;
      continue;
    }
    const marker = buffer[offset + 1];
    offset += 2;
    if (marker === 0xD8 || marker === 0xD9) continue;
    const length = buffer.readUInt16BE(offset);
    if (length < 2) return null;
    if (
      (marker >= 0xC0 && marker <= 0xC3)
      || (marker >= 0xC5 && marker <= 0xC7)
      || (marker >= 0xC9 && marker <= 0xCB)
      || (marker >= 0xCD && marker <= 0xCF)
    ) {
      return {
        width: buffer.readUInt16BE(offset + 5),
        height: buffer.readUInt16BE(offset + 3)
      };
    }
    offset += length;
  }
  return null;
}

function pngSize(buffer) {
  const signature = '89504e470d0a1a0a';
  if (buffer.subarray(0, 8).toString('hex') !== signature) return null;
  return {
    width: buffer.readUInt32BE(16),
    height: buffer.readUInt32BE(20)
  };
}

function imageInfo(file) {
  const relative = normalizeKey(file);
  const absolute = path.join(ROOT, relative);
  if (!fs.existsSync(absolute)) {
    throw new Error(`${relative}: image introuvable.`);
  }
  const buffer = fs.readFileSync(absolute);
  const ext = path.extname(relative).toLowerCase();
  const size = ext === '.png' ? pngSize(buffer) : jpegSize(buffer);
  if (!size) {
    throw new Error(`${relative}: dimensions image illisibles.`);
  }
  return { width: size.width, height: size.height, bytes: buffer.length };
}

function buildManifest() {
  const recipes = loadRecipes();
  const files = new Set([
    'assets/base-du-site.png',
    'assets/base-principale-fond-site.jpg',
    'assets/cook-note.png',
    'assets/cook-note-white.png'
  ]);
  DAY_ART_FILES.forEach(file => files.add(file));

  Object.values(recipes).forEach(recipe => {
    const image = recipe?.image;
    if (!image || !image.startsWith('/assets/recipe-images-optimized/')) return;
    files.add(normalizeKey(image));
    files.add(recipeCardPath(image));
    files.add(imageSourcePath(image));
  });

  return Object.fromEntries(
    Array.from(files)
      .sort((left, right) => left.localeCompare(right, 'en'))
      .map(file => [file, imageInfo(file)])
  );
}

function manifestText() {
  return `// Generated by scripts/generate-image-manifest.js. Do not edit manually.\nwindow.COOK_NOTE_IMAGE_MANIFEST = Object.freeze(${JSON.stringify(buildManifest(), null, 2)});\n`;
}

function readCurrentManifest() {
  const context = { window: {}, Object };
  vm.createContext(context);
  vm.runInContext(fs.readFileSync(OUT_FILE, 'utf8'), context, { filename: OUT_FILE });
  return context.window.COOK_NOTE_IMAGE_MANIFEST || {};
}

function stableValue(value) {
  if (Array.isArray(value)) return value.map(stableValue);
  if (!value || typeof value !== 'object') return value;
  return Object.fromEntries(
    Object.entries(value)
      .sort(([left], [right]) => left.localeCompare(right, 'en'))
      .map(([key, item]) => [key, stableValue(item)])
  );
}

const next = manifestText();
if (CHECK_ONLY) {
  const current = fs.existsSync(OUT_FILE) ? readCurrentManifest() : {};
  if (JSON.stringify(stableValue(current)) !== JSON.stringify(stableValue(buildManifest()))) {
    console.error('assets/image-manifest.js n est pas a jour. Lancer npm run generate:image-manifest.');
    process.exit(1);
  }
  console.log('Validation manifest images OK.');
} else {
  fs.writeFileSync(OUT_FILE, next);
  console.log(`Manifest images genere: ${path.relative(ROOT, OUT_FILE)}`);
}
